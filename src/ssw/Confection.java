package ssw;

class Confection {
    private int weight;
    private int sugar;
    private String manufacturer;

    private int id;

    int getWeight() {
        return weight;
    }

    void setWeight(int weight) {
        this.weight = weight;
    }

    int getSugar() {
        return sugar;
    }

    void setSugar(int sugar) {
        this.sugar = sugar;
    }

    String getManufacturer() {
        return manufacturer;
    }

    void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    int getId() {
        return id;
    }

    void setId(int id) {
        this.id = id;
    }
}
